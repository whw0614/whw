	$cipher= 'AES-256-CBC';
	$iv = random_bytes(openssl_cipher_iv_length($cipher));
	$value = openssl_encrypt(serialize($my_enc),$cipher, $key, 0, $iv);
	$mac = hash_hmac('sha256', base64_encode($iv).$value, $key);
